public class Coordinate {
    private final float x,z;
//Holds vector for Language R
    public Coordinate(float x, float z) {
        this.x = x;
        this.z = z;
    }

    public float getX() {
        return x;
    }

    public float getZ() {
        return z;
    }

    
    
}
